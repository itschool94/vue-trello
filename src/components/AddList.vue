<template>
  <div class="add-list">
    <input type="text" v-if="isAddList" class="form-control" v-model="inputTitle" ref="inputTitle" @blur="restore" @keyup.enter="onSubmitTitle">
    <a href="" v-else @click.prevent="onAddList">&plus; Add another list</a>
  </div>
</template>
<script>
import { mapState, mapActions } from 'vuex';

export default {
  data() {
    return {
      inputTitle: '',
      isAddList: false
    }
  },

  computed: {
    ...mapState({
      board : 'board'
    })
  },

  methods: {
    ...mapActions([
      'ADD_LIST'
    ]),

    onAddList() {
      this.isAddList = true;
      this.$nextTick( () => this.$refs.inputTitle.focus() )
    },

    onSubmitTitle() {
      this.inputTitle = this.inputTitle.trim();
      if( !this.inputTitle ) return this.restore();

      const title = this.inputTitle;
      const boardId = this.board.id;

      const lastList = this.board.lists[ this.board.lists.length - 1 ]; // 마지막 lists
      const pos = lastList ? lastList.pos * 2 : 65535

      this.ADD_LIST( { title, boardId, pos })
      .then( () => this.restore() )
    },

    restore() {
      this.isAddList = false;
      this.inputTitle = '';
    },

  }
}
</script>

